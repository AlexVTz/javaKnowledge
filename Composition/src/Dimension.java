
public class Dimension {
	
	@SuppressWarnings("unused")
	private int width;
	@SuppressWarnings("unused")
	private int height;
	@SuppressWarnings("unused")
	private int depth;
	
	public Dimension(int width, int height, int depth) {
		super();
		this.width = width;
		this.height = height;
		this.depth = depth;
	}
	
	
	
}
