
public class Resolution {
	
	private int width;
	private int hwight;
	/**
	 * @param width
	 * @param hwight
	 */
	public Resolution(int width, int hwight) {
		super();
		this.width = width;
		this.hwight = hwight;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHwight() {
		return hwight;
	}
	public void setHwight(int hwight) {
		this.hwight = hwight;
	}
	
	
	
}
